from typing import Protocol, runtime_checkable


@runtime_checkable
class PasswordHasher(Protocol):
    """Порт сервиса хеширования паролей.

    Определяет контракт для хеширования паролей и проверки
    соответствия пароля хешу. Реализация может использовать
    различные алгоритмы хеширования (Argon2id, bcrypt и т.д.).

    Notes
    -----
    Порт не зависит от конкретной технологии хеширования.
    Выбор алгоритма является ответственностью Infrastructure Layer.
    """

    def hash(self, password: str) -> str:
        """Вычисляет хеш пароля.

        Parameters
        ----------
        password : str
            Пароль в открытом виде для хеширования.

        Returns
        -------
        str
            Строковое представление хеша пароля.
        """
        ...

    def verify(self, password: str, hash: str) -> bool:
        """Проверяет соответствие пароля хешу.

        Parameters
        ----------
        password : str
            Пароль в открытом виде для проверки.
        hash : str
            Хеш, с которым требуется сравнить пароль.

        Returns
        -------
        bool
            ``True``, если пароль соответствует хешу, иначе ``False``.
        """
        ...

    # TODO: Необходимо определить, как порт должен реагировать на
    #       случай, когда хеш не может быть распознан ни одним
    #       из доступных алгоритмов. Требуется проработка контракта.
    def verify_and_update(self, password: str, hash: str) -> tuple[bool, str | None]:
        """Проверяет соответствие пароля хешу и при необходимости
        обновляет хеш до актуального алгоритма.

        Parameters
        ----------
        password : str
            Пароль в открытом виде для проверки.
        hash : str
            Хеш, с которым требуется сравнить пароль.

        Returns
        -------
        tuple[bool, str | None]
            Кортеж из двух элементов:

            - ``True``, если пароль соответствует хешу, иначе ``False``.
            - Новый хеш, если алгоритм или параметры устарели,
              иначе ``None``.
        """
        ...
