from typing import Protocol, runtime_checkable


@runtime_checkable
class BaseRepository[IdType, EntityType](Protocol):
    """Базовый порт репозитория.

    Определяет общий контракт для всех репозиториев приложения.
    Содержит стандартные операции CRUD: получение по идентификатору,
    массовое получение, удаление по идентификатору и массовое удаление.

    Parameters
    ----------
    IdType : TypeVar
        Тип идентификатора сущности (например, ``UUID``, ``int``).
    EntityType : TypeVar
        Тип сущности, с которой работает репозиторий.

    Notes
    -----
    Данный порт не предполагает операции создания/обновления, так как
    они специфичны для каждой сущности и должны быть объявлены
    в наследуемом интерфейсе.
    """

    async def get_by_id(self, id: IdType) -> EntityType:
        """Получить сущность по идентификатору.

        Parameters
        ----------
        id : IdType
            Идентификатор сущности.

        Returns
        -------
        EntityType
            Найденная сущность.

        Raises
        ------
        NotFoundException
            Если сущность с указанным идентификатором не найдена.
        """
        ...

    async def get_many_by_ids(self, ids: list[IdType]) -> list[EntityType]:
        """Получить несколько сущностей по списку идентификаторов.

        Parameters
        ----------
        ids : list[IdType]
            Список идентификаторов сущностей.

        Returns
        -------
        list[EntityType]
            Список найденных сущностей. Порядок не гарантируется.
            Несуществующие идентификаторы игнорируются.
        """
        ...

    async def delete_by_id(self, id: IdType) -> bool:
        """Удалить сущность по идентификатору.

        Parameters
        ----------
        id : IdType
            Идентификатор сущности для удаления.

        Returns
        -------
        bool
            ``True``, если сущность была удалена,
            ``False``, если сущность с указанным идентификатором не найдена.
        """
        ...

    async def delete_many_by_ids(self, ids: list[IdType]) -> int:
        """Удалить несколько сущностей по списку идентификаторов.

        Parameters
        ----------
        ids : list[IdType]
            Список идентификаторов сущностей для удаления.

        Returns
        -------
        int
            Количество удалённых сущностей.
        """
        ...
