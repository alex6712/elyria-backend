from uuid import UUID

from sqlalchemy import Column
from sqlalchemy.types import Uuid


def ownership_columns() -> tuple[Column[UUID]]:
    """Создать колонки владения сущностью (ownership).

    Добавляет в таблицу идентификатор пользователя-владельца записи.
    Используется для сущностей, доступ к которым ограничен
    их владельцем (например, приватные ресурсы).

    Колонка хранит только идентификатор пользователя и не содержит
    ограничения внешнего ключа. Это позволяет сохранить независимость
    ограниченных контекстов и избежать жёсткой связи между их схемами
    данных. Проверка существования владельца и поддержание ссылочной
    согласованности выполняются на уровне приложения.

    Каждый вызов возвращает новый объект `Column`, поскольку
    SQLAlchemy привязывает колонку к конкретной таблице при первом
    использовании.

    Returns
    -------
    tuple[Column[UUID]]
        Кортеж из одной колонки:

        - **owner_id** : `UUID`, not null.
            Идентификатор пользователя-владельца ресурса.
            Используется для определения владельца записи и проверки
            прав доступа.
    """
    return (
        Column(
            "owner_id",
            Uuid(as_uuid=True),
            nullable=False,
            comment="UUID пользователя-владельца ресурса",
        ),
    )
