from enum import StrEnum, auto


class APICode(StrEnum):
    """Класс-перечисление для представления кодов ответов API.

    Представляет собой единое перечисление всех возможных
    ответов API с их описанием и use cases.

    Значения генерируются автоматически через ``auto()`` и совпадают
    с именем члена enum (UPPER_SNAKE_CASE), а не с его lowercase-версией,
    как это происходит в ``StrEnum`` по умолчанию. Это сделано для
    совместимости с фронтендом, который хранит коды в верхнем регистре.
    """

    @staticmethod
    def _generate_next_value_(
        name: str,
        start: int,  # noqa: ARG004
        count: int,  # noqa: ARG004
        last_values: list[str],  # noqa: ARG004
    ) -> str:
        """Переопределяет генерацию значения для ``auto()``.

        По умолчанию ``StrEnum._generate_next_value_`` возвращает
        ``name.lower()``. Здесь вместо этого возвращается ``name.upper()``,
        чтобы значение члена enum совпадало с его именем в верхнем
        регистре (например, ``SUCCESS = auto()`` -> ``SUCCESS.value == "SUCCESS"``).

        Parameters
        ----------
        name : str
            Имя члена enum (например, "SUCCESS").
        start : int
            Стартовое значение для auto() (не используется).
        count : int
            Количество уже определённых членов (не используется).
        last_values : list[str]
            Список уже сгенерированных значений (не используется).

        Returns
        -------
        str
            Строковое значение члена enum в верхнем регистре.
        """
        return name.upper()

    SUCCESS = auto()
    """Когда всё хорошо, ничего не сломалось, а пользователь - молодец."""

    I_AM_A_TEAPOT = auto()
    """Я - чайник и не могу сварить кофе."""

    RESOURCE_NOT_FOUND = auto()
    """При попытке доступа к несуществующему ресурсу."""

    VALIDATION_ERROR = auto()
    """При ошибке валидации переданных клиентом данных."""

    NOTHING_TO_UPDATE = auto()
    """Вызывается, когда PATCH-запрос не содержит ни одного поля
    для обновления - все поля DTO остались ``UNSET``."""

    INCORRECT_USERNAME_PASSWORD = auto()
    """Если по ``username`` не найден пользователь или ``password``
    и хеш пароля в БД не совпадают."""

    INCORRECT_PASSWORD = auto()
    """Текущий пароль не совпадает с сохранённым в БД."""

    NEW_PASSWORD_SAME_AS_OLD = auto()
    """Новый пароль совпадает с текущим сохранённым в БД."""

    PASSWORD_UPDATE_FAILED = auto()
    """Обновление пароля не затронуло ни одной строки в БД."""

    TOKEN_NOT_PASSED = auto()
    """Вызывается при условии, что JWT не передан в заголовках запроса."""

    INVALID_TOKEN = auto()
    """Неверный токен.

    Используется в случаях:
    1. Если в ``payload`` токена отсутствует хотя бы один из ключей ``sub`` и ``exp``;
    2. если значение по ключу ``sub`` (user_id) не существует в БД;
    3. не получается проверить подпись токена (подпись токена недействительна);
    4. переданный токен обновления не совпадает с хешем в БД.
    """

    TOKEN_SIGNATURE_EXPIRED = auto()
    """Подпись токена действительна, однако вышло время жизни токена."""

    TOKEN_REVOKED = auto()
    """Если переданный клиентом токен был отозван и находится в чёрном списке."""

    INVALID_IDEMPOTENCY_KEY = auto()
    """Ключ идемпотентности не совпадает с ожидаемый форматом (UUIDv4)."""

    IDEMPOTENCY_CONFLICT = auto()
    """Запрос по предоставленному ключу идемпотентности уже обрабатывается."""

    UNIQUE_CONFLICT = auto()
    """При попытке создать новую запись по уже существующему уникальному ключу."""

    COUPLE_NOT_SELF = auto()
    """При попытке создать пользовательскую пару с самим собой."""

    ALBUM_NOT_FOUND = auto()
    """При попытке получить медиаальбом по несуществующему набору параметров."""

    FILE_NOT_FOUND = auto()
    """При попытке получить медиафайл по несуществующему набору параметров."""

    FILE_UPLOAD_PENDING = auto()
    """При попытке скачать файл, загрузка которого ещё не завершена (статус PENDING)."""

    FILE_UPLOAD_FAILED = auto()
    """При попытке скачать файл, загрузка которого завершилась
    ошибкой (статус FAILED)."""

    FILE_DELETED = auto()
    """При попытке скачать файл, который был явно удалён
    пользователем (статус DELETED)."""

    FILE_PRESIGNED_URL_GENERATION_FAILED = auto()
    """Presigned URL для загрузки/скачивания файла не была
    сгенерирована из-за ошибки."""

    UNSUPPORTED_FILE_TYPE = auto()
    """При попытке загрузить файл с необрабатываемым типом."""

    UPLOAD_NOT_COMPLETED = auto()
    """При подтверждении клиентом загрузки файл, файл не найден
    в объектном хранилище."""

    RATE_LIMIT_EXCEEDED = auto()
    """Превышено максимальное количество запросов за единицу времени."""

    INTERNAL_SERVER_ERROR = auto()
    """На сервере произошла неожиданная ошибка.
    В любом случае, клиент не должен знать какая."""
