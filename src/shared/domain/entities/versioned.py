class Versioned:
    """Класс-примесь, добавляющая агрегату поддержку optimistic locking.

    Позволяет отслеживать версию агрегата для реализации оптимистичной
    блокировки (optimistic locking). Каждый раз при успешном сохранении
    изменений в репозитории версия увеличивается на 1. Это гарантирует,
    что никто не перезапишет изменения, внесённые другим concurrent
    запросом, между чтением и записью агрегата.

    Версия не является бизнес-свойством сущности, а лишь технической
    характеристикой для контроля конкурентного доступа. Use case не
    должен знать о существовании этого поля.

    Attributes
    ----------
    version : int
        Текущая версия агрегата. Начинается с 1 для новой сущности
        и увеличивается на 1 при каждом успешном сохранении изменений
        через репозиторий.

    Notes
    -----
    Метод ``upgrade`` вызывается исключительно репозиторием после
    успешного выполнения операции сохранения сущности в хранилище,
    чтобы объект Python оставался синхронизирован с состоянием
    хранилища.
    """

    version: int

    def upgrade(self) -> None:
        """Увеличить версию агрегата на 1.

        Вызывается репозиторием после успешного выполнения
        обновления с проверкой версии. Приводит значение атрибута
        ``version`` в соответствие с состоянием хранилища.

        Notes
        -----
        Метод не является публичным API для use case и не должен
        вызываться из Application или Presentation Layer.
        """
        self.version += 1
