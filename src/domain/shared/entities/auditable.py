from datetime import datetime, timezone


class Auditable:
    """Класс-примесь, предоставляющий сущности временные метки аудита.

    Добавляет поля ``created_at`` и ``updated_at``, а также метод
    ``_touch`` для обновления метки последнего изменения. Позволяет
    вынести логику аудита в отдельный класс-примесь и использовать
    её в нескольких иерархиях наследования.

    Attributes
    ----------
    created_at : datetime
        Дата и время создания сущности.
    updated_at : datetime | None
        Дата и время последнего изменения сущности. Может быть
        ``None``, если с момента создания не было изменений.

    Notes
    -----
    Класс не содержит бизнес-логики и предназначен исключительно для
    отслеживания времени создания и изменения сущности. Применяется
    в качестве примеси совместно с ``Identifiable`` или другими
    классами, обеспечивающими идентичность.
    """

    created_at: datetime
    updated_at: datetime | None

    def _touch(self, at: datetime | None = None) -> None:
        """Обновить временную метку последнего изменения.

        Parameters
        ----------
        at : datetime | None, optional
            Момент времени для установки в качестве ``updated_at``.
            Если не передан, используется текущее время. Явная
            передача нужна там, где ``updated_at`` должен быть
            согласован с другим полем, изменяемым в том же вызове.
        """
        self.updated_at = at or datetime.now(timezone.utc)
