from enum import StrEnum, auto


class DomainContext(StrEnum):
    """Категории предметной области, используемые для классификации доменных ошибок.

    Значение категории определяет, к какому функциональному разделу
    (bounded context) относится исключение. Категории используются
    для структурированного логирования, мониторинга, трассировки ошибок
    и их преобразования во внешние представления (например, HTTP- или
    gRPC-ответы).

    Notes
    -----
    Категория отражает принадлежность ошибки к определённой части
    предметной области и не описывает её тип. Например, ошибки вида
    ``NotFound`` или ``AlreadyExists`` могут встречаться в любой категории.
    """

    IAM = auto()
    """Аутентификация и управление учётными данными."""

    USER = auto()
    """Пользователи и их профиль."""

    COUPLE = auto()
    """Логика взаимодействия пары."""

    MEDIA = auto()
    """Файлы, альбомы и другие медиаобъекты."""

    NOTE = auto()
    """Заметки."""
